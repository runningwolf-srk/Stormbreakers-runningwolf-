    text: `The snow fell sideways in the Valley of Bones.

Not the gentle kind that covers sin. The kind that cuts. The kind that blinds.

Astrid Stormborn pulled her fur cloak tighter and spat blood into the white. Third day of patrol. Third day of nothing. The raiders had gone quiet. Too quiet.

"Your lip's split again," said Kael, riding beside her. He was sixteen, all knees and elbows, but he held a spear like he was born with it.

"Bit it," Astrid lied. She'd been grinding her teeth in her sleep. Dreams of fire. Dreams of horns.

Below them, the village of Skall slept. Smoke from a hundred hearths rose straight up — no wind. The Long Peace, they called it. Three hundred winters without a great war.

The elders said it was a gift from the gods. Astrid's father said it was because everyone was too tired to fight.

A raven cawed. Once. Twice. Three times.

Kael stiffened. "That's Bjorn's bird."

The raven did not fly to them. It flew past. Toward the mountain. Toward the Hall of the Stormkeepers.

Astrid's stomach dropped. Bjorn One-Eye only sent his raven for one reason.

"Ride," she said.

They kicked their horses into a gallop. The village blurred past. Children stopped their games to watch. Old women crossed themselves.

The Hall of the Stormkeepers was carved into the mountain itself. Stone doors twenty feet tall. Doors that had not opened in Astrid's lifetime.

They were open now.

Inside, the great hall was dark except for one shaft of light from the oculus above. It fell directly on the horn.

The Horn of War.

Forged, the legends said, from the skull of Fafnir, the last dragon. Banded in black iron. Runes carved deep.

It hung on chains above the empty throne. Silent for three centuries.

Kings had tried to wake it. Jarl Eriksson blew until his lungs bled. King Harald the Mad struck it with his warhammer. The horn remained mute.

Now it was moving.

Not swinging. Vibrating. A hum so low Astrid felt it in her teeth.

Chieftain Bjorn One-Eye stood beneath it. One eye milky with age, the other sharp as a spearpoint. He did not look at Astrid or Kael. He looked at the horn.

"How long?" Astrid asked.

"Since midnight," Bjorn said. His voice was gravel. "Started as a whisper. Now..."

The hum became a note.

Not loud. Deep.

The kind of sound that makes stone remember it was once liquid.

The chains holding the horn snapped.

It did not fall. It hovered.

Then it blew.

No mouth to lips. No breath. The horn blew itself.

One note.

The stone floor cracked. The oculus shattered. The torches in their sconces blew out.

Outside, every dog in the valley howled.

Inside, every warrior dropped to their knees. Not in worship. In recognition.

The Long Peace was over.

When the ringing stopped, Bjorn finally looked at Astrid.

"Gather the warband," he said.

"Which enemy?" Kael asked, his voice shaking.

Bjorn walked to the empty throne and touched the armrest. Dust came away on his fingers.

"The horn doesn't blow for raiders, boy. It blows for reckoning."

Astrid drew her axe. "Then who—"

"The horn blew itself," Bjorn said. "So I think we should ask it what it wants."

He pointed to the shadows behind the throne. Where the light never reached.

Where something had been waiting for three hundred winters.

"Something older than kings," Bjorn whispered. "Something that remembers what we were before we learned to fear."

The horn, now silent again, slowly lowered itself back onto its hooks.

But the chains did not regrow.

War had come.`,
